let numeroMes = Number(prompt('Digite um número de 1 a 12'))

switch (numeroMes) {
    case 1:
        console.log(`O mês ${numeroMes} é Janeiro`)
        break;
    case 2:
        console.log(`O mês ${numeroMes} é Fevereiro`)
        break;
    case 3:
        console.log(`O mês ${numeroMes} é Março`)
        break;
    case 4:
        console.log(`O mês ${numeroMes} é Abril`)
        break;
    case 5:
        console.log(`O mês ${numeroMes} é Maio`)
        break;
    case 6:
        console.log(`O mês ${numeroMes} é Junho`)
        break;
    case 7:
        console.log(`O mês ${numeroMes} é Julho`)
        break;
    case 8:
        console.log(`O mês ${numeroMes} é Agosto`)
        break;
    case 9:
        console.log(`O mês ${numeroMes} é Setembro`)
        break;
    case 10:
        console.log(`O mês ${numeroMes} é Outubro`)
        break;
    case 11:
        console.log(`O mês ${numeroMes} é Novembro`)
        break;
    case 12:
        console.log(`O mês ${numeroMes} é Dezembro`)
        break;
}