var diaSemanaQ14 = Number(prompt('Digite o número do dia da semana por extenso'));
switch (diaSemanaQ14) {
    case 1:
        console.log("Domingo");
        break;
    case 2:
        console.log("Segunda");
    case 3:
        console.log("Ter\u00E7a");
        break;
    case 4:
        console.log("Quarta");
        break;
    case 5:
        console.log("Quinta");
        break;
    case 6:
        console.log("Sexta");
        break;
    case 7:
        console.log('Sábado');
        break;
}
