let numeroPositNegat = Number(prompt("Digite um número inteiro: "))

if (numeroPositNegat > 0) {
    console.log(`O número ${numeroPositNegat} é positivo`)
}else if (numeroPositNegat === 0){
    console.log(`O número ${numeroPositNegat} neutro`)
}else {
    console.log(`O número ${numeroPositNegat} negativo`)
} 